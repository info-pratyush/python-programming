txt = "Microsoft 365"
print(txt)

# String checks
print(txt.isalpha())  # False
print(txt.isdigit())  # False
print(txt.isalnum())  # False
print(txt.islower())  # False
print(txt.isupper())  # False
print(txt.isspace())  # False
print(txt.startswith("Microsoft"))  # True
print(txt.endswith("365"))  # True
