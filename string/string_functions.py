
name = "pratyush"
print(name)

# String functions
print(name.upper())  # PRATYUSH
print(name.lower())  # pratyush
print(name.capitalize())  # Pratyush
print(name.title())  # Pratyush
print(name.strip())  # pratyush
print(name.replace("a", "A"))  # prAtyush
print(name.split("a"))  # ['pr', 'tyush']
print(name.find("t"))  # 2
print(name.count("a"))  # 1




