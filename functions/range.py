# Range with one argument

x = range (10)

print(x) #output: range(0, 10)

print(list(x))

# Range with two arguments, first is start value and second is stop value

y = range(3,10)

print(y)
print(list(y))

# Range with three arguments, here third argument represents the step value

z = range(3, 10, 2)

print(z)
print(list(z))
print(len(z))

