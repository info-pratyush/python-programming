#Assignment operators are used to assign values to variables. 

print("Assignment Operators")
x = 5
print("x =", x)

x += 3
print("x += 3 -> x =", x)

x -= 2
print("x -= 2 -> x =", x)

x *= 4
print("x *= 4 -> x =", x)

x /= 2
print("x /= 2 -> x =", x)

x //= 3
print("x //= 3 -> x =", x)
x %= 2
print("x %= 2 -> x =", x)

