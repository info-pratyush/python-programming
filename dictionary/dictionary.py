# Dictionary in Python stores key-value pairs. It is a collection which is ordered, changeable and does not allow duplicates. Dictionaries are written with curly brackets.

# Creating a Dictionary

my_dict = {
    "name" : "Pratyush",
    "age" : 18,
    "city" : "Ambala"
}

print(my_dict)
print("Length of the dictionary: ", len(my_dict))

# Dictionary constructor
my_dict2 = dict(name="Pratyush", age=18, city="Ambala")
print(my_dict2)
