# List in python is ordered, mutable and allow duplicate elements. It is defined by square brackets [].

a = [10, 20, 30, 40, 50] # creating List
print(a)

# List can contain different types of data.

b = [10, "Hello", 3.14, True]
print(b)

# List can also contain other lists.

c = [10, [20, 30], "Python", False]
print(c)

