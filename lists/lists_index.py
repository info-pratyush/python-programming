# Lists items are indexed by their position in the list, starting with 0. so youn can access any item by referring to its index number.

a = [10, 20, 30, 40, 50]
print(a[0]) # 10
print(a[1]) # 20
print(a[2]) # 30

# Negative indexing = access items from the end of the list.

print(a[-1]) # 50
print(a[-2]) # 40