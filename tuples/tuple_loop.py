my_tuple = ("apple", "banana", "cherry")
for item in my_tuple:
    print(item)

this_tuple = ("apple", "banana", "cherry")
for i in range(len(this_tuple)):
    print(this_tuple[i])

# Looping through a tuple using while loop

my_tuple = ("apple", "banana", "cherry")
i = 0
while i < len(my_tuple):
    print(my_tuple[i])
    i += 1
        

