# Tuple packing and unpacking

# Tuple packing is the process of assigning multiple values to a single variable. It is done by separating the values with commas.

# Tuple unpacking is the process of assigning the values of a tuple to multiple variables. It is done by separating the variables with commas.

# Tuple packing
my_tuple = "apple", "banana", "cherry"
print(my_tuple)

# Tuple unpacking
fruit1, fruit2, fruit3 = my_tuple
print(fruit1)
print(fruit2)
print(fruit3)
